#include <stdio.h>

int main() {
  int a[3][3];
  int n;
  int b[11];
  int c[3][3] = {0};
  for (int i = 0; i < 3; i++) {
    for (int j = 0; j < 3; j++) {
      scanf("%d", &a[i][j]);
    }
  }
  scanf("%d", &n);
  for (int i = 0; i < n; i++) {
    scanf("%d", &b[i]);
  }

  for (int i = 0; i < 3; i++) {
    for (int j = 0; j < 3; j++) {
      for (int k = 0; k < 10; k++) {
        if (a[i][j] == b[k]) {
          c[i][j] = 1;
        }
      }
    }
  }

  if ((c[0][0] == 1 && c[0][1] == 1 && c[0][2] == 1) ||
      (c[1][0] == 1 && c[1][1] == 1 && c[1][2] == 1) ||
      (c[2][0] == 1 && c[2][1] == 1 && c[2][2] == 1) ||
      (c[0][0] == 1 && c[1][0] == 1 && c[2][0] == 1) ||
      (c[0][1] == 1 && c[1][1] == 1 && c[2][1] == 1) ||
      (c[0][2] == 1 && c[1][2] == 1 && c[2][2] == 1) ||
      (c[0][0] == 1 && c[1][1] == 1 && c[2][2] == 1) ||
      (c[0][2] == 1 && c[1][1] == 1 && c[2][0] == 1)) {
    printf("Yes\n");
  } else {
    printf("No\n");
  }

  return 0;
}
